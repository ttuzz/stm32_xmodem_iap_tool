void Main_Menu(void)
{
  uint8_t key = 0;


  uart_transmit_str((uint8_t *)"\r\n=================== Main Menu ============================\r\n\n");
  while (1)
  {
    uart_transmit_str((uint8_t *)"  Download image to the internal Flash ----------------- 1\r\n\n");
    uart_transmit_str((uint8_t *)"  Execute the loaded application ----------------------- 3\r\n\n");
    uart_transmit_str((uint8_t *)"==========================================================\r\n\n");

    /* Clean the input path */
    //__HAL_UART_FLUSH_DRREGISTER(&UartHandle);
    //__HAL_UART_CLEAR_IT(&UartHandle, UART_CLEAR_OREF);

    /* Receive key */
    HAL_UART_Receive(&UartHandle, &key, 1, HAL_MAX_DELAY);



    switch (key)
    {
    case '1' :
      /* Download user application in the Flash */
    	uart_transmit_str((uint8_t*)"Please send a new binary file with Xmodem protocol to update the firmware.\n\r");
    	xmodem_receive();
      break;
    case '2' :

      break;
    case '3' :

    	flash_jump_to_app();
      break;
    case '4' :

      break;
	default:
		uart_transmit_str((uint8_t *)"Invalid Number ! ==> The number should be either 1, 2, 3 or 4\r");
	break;
    }
  }
}
